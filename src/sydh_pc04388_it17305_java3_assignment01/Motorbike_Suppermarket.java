    public void sort(int i) {
        Comparator<SanPham> cmpMaxe = new Comparator<SanPham>() {
            @Override
            public int compare(SanPham sp1, SanPham sp2) {
                return sp1.getMSX().compareTo(sp2.getMSX());
            }
        };
        Comparator<SanPham> cmpGia = new Comparator<SanPham>() {
            @Override
            public int compare(SanPham sp01, SanPham sp02) {
                if (sp01.getGia() > sp02.getGia()) {
                    return 1;
                } else if (sp01.getGia() < sp02.getGia()) {
                    return -1;
                } else {
                    return 0;
                }
            }
        };

        if (rdomsx.isSelected() && i == 0) {
            Collections.sort(list, cmpMaxe);
            fillTable();
            return;
        }

        if (rdogia.isSelected() && i == 0) {
            Collections.sort(list, cmpGia);
            fillTable();
            return;
        }
        if (rdomsx.isSelected() && i == 1) {
            Collections.sort(list, cmpMaxe.reversed());
            fillTable();
            return;
        }

        if (rdogia.isSelected() && i == 1) {
            Collections.sort(list, cmpGia.reversed());
            fillTable();
            return;
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn giá trị sắp xếp!", "Error", JOptionPane.WARNING_MESSAGE);
        }

    }

