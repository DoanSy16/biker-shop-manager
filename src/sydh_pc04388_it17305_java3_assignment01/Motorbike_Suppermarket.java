            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
//            String url = "jdbc:sqlserver://localhost:1433;databaseName=QLBanxe";
            java.sql.Connection con = DriverManager.getConnection(url, user, pass);
