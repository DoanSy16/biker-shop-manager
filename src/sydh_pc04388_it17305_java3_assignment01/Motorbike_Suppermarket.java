    public void fillTable() {
        while (tblModel.getRowCount() > 0) {
            tblModel.removeRow(0);
        }
        String LOAIXE = null;
        for (SanPham sp : list) {
            if (sp.getLoaiXe().equalsIgnoreCase("true")) {
                LOAIXE = "Xe số";
            } else if (sp.getLoaiXe().equalsIgnoreCase("false")) {
                LOAIXE = "Tay ga";
            }
            tblModel.addRow(new Object[]{sp.getMSX(), sp.getHangXe(), sp.getTen(), LOAIXE, sp.getPhanKhoi(), sp.getSoKhung(), sp.getSoMay(), sp.getMauSon(), sp.getChoNgoi(), sp.getNoiSanxuat(), sp.getGia()});
        }
    }

